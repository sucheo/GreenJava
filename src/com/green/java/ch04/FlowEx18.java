package com.green.java.ch04;

import java.util.Scanner;

public class FlowEx18 {

    public static void main(String[] args) {

        for (int i = 2; i <10; i++) {

            for(int j = 1; j<10; j++){
                System.out.printf("%d X %d = %d",i,j,i*j);
                System.out.println();
        }
            System.out.println();

        }

    }

}
